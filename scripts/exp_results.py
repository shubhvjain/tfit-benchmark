def combine_performance_scores(exp_name, exp_data, result_data, rerun, n_jobs=4):
    """
    """
    dsets = result_data.get("datasets",exp_data["datasets"])
    result_folder = get_output_path()/ exp_name/"_results"
    result_folder.mkdir(parents=True,exist_ok=True)
    result_file = result_folder / f"{result_data['name']}.csv"
    if result_file.exists() and not rerun:
        raise FileExistsError(result_file)
    results = []
    for f in result_data["files"]:
        for d in dsets:
            index_file = get_output_path() / exp_name /d/f["tool"]/f"result_{f['result']}_indices.csv"
            if not index_file.exists():
                raise FileNotFoundError(index_file)
            
            data = pd.read_csv(index_file)
            data["exp_name"] = exp_name
            data["dataset"]= d
            data["tool"] = f['tool']
            data['result_name'] = f['result']

            results.append(data)

    df = pd.concat(results)
    df.to_csv(result_file,index=False)
    print(result_file,"saved")

