def get_exp_file(id):
    """Load experiment configuration file."""
    file_path = get_exp_path() / f"{id}.json"
    data = json.loads(file_path.read_text())
    return data


def main():
    parent_parser = argparse.ArgumentParser(add_help=False)
    parent_parser.add_argument(
        "--id",     required=True, help="experiment name")
    parent_parser.add_argument("--name",   required=True, help="result id")
    parent_parser.add_argument(
        "--rerun",  action="store_true", help="whether to rerun the experiment")
    parent_parser.add_argument("--n_jobs", type=int, default=8,
                               help="number of parallel workers for index loading (default: 8)")

    parser = argparse.ArgumentParser(
        description="Generate result",
        parents=[parent_parser]
    )
    args = parser.parse_args()

    if args.id and args.name:
        exp = get_exp_file(args.id)
        result = next(filter(lambda d: d.get("name") ==
                      args.name, exp.get("results", [])), None)
        if result is None:
            raise ValueError("Result not found")
        if result.get("type") not in RESULT_REGISTRY:
            raise ValueError("Invalid result type")
        RESULT_REGISTRY[result.get("type")](
            args.id, exp, result, args.rerun, n_jobs=args.n_jobs)
    else:
        raise ValueError("provide id and name")

if __name__ == "__main__":
    main()